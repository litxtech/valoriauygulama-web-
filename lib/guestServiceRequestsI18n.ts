function pack(): Record<ServiceKey, string> {
  const l = resolveAppLang();
  return GUEST_SERVICE_I18N[l] ?? GUEST_SERVICE_I18N.en;
}

export function guestServiceText(key: ServiceKey): string {
  const d = pack();
  return d[key] ?? GUEST_SERVICE_I18N.tr[key] ?? key;
}

export function guestServiceTypeLabel(type: GuestServiceRequestType): string {
  return guestServiceText(`type_${type}` as ServiceKey);
}

export function guestServiceStatusLabel(status: GuestServiceRequestStatus): string {
  return guestServiceText(`status_${status}` as ServiceKey);
}
