export type FeedSharedTextKey = keyof typeof UI.tr;

export function feedSharedText(key: FeedSharedTextKey, vars?: Record<string, string | number>): string {
  const fromPulse = pulseText(key, vars);
  if (fromPulse != null) return fromPulse;
  const l = lang();
  let s = (UI[l][key] as string) ?? (UI.en[key] as string) ?? (UI.tr[key] as string) ?? String(key);
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{\\{${k}\\}\\}`, 'g'), String(v));
    }
  }
  return s;
}

