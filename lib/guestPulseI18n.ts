export type GuestPulseTextKey = keyof typeof GUEST_PULSE_I18N.tr;

export function guestPulseText(key: GuestPulseTextKey, vars?: Record<string, string | number>): string {
  const l = lang();
  let s =
    (GUEST_PULSE_I18N[l][key] as string) ??
    (GUEST_PULSE_I18N.en[key] as string) ??
    (GUEST_PULSE_I18N.tr[key] as string) ??
    String(key);
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{\\{${k}\\}\\}`, 'g'), String(v));
    }
  }
  return s;
}

/** feedSharedText ile birleşik: nabız anahtarları önce buradan */
export function pulseText(key: string, vars?: Record<string, string | number>): string | null {
  if (!(key in GUEST_PULSE_I18N.tr)) return null;
  return guestPulseText(key as GuestPulseTextKey, vars);
}
